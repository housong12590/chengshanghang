package com.jmm.csg.bean;


public class IntegralItem {

    private int icon;
    private String text;

    public IntegralItem(int icon, String text) {
        this.icon = icon;
        this.text = text;
    }

    public int getIcon() {
        return icon;
    }

    public String getText() {
        return text;
    }
}
